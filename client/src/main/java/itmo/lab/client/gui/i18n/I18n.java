    /** Возвращает локализованную строку по ключу для текущего языка. */
    public static String t(String key) {
        Map<String, String> map = STRINGS.get(lang.get());
        if (map == null) map = STRINGS.get(Lang.RU);
        String val = map.get(key);
        return val != null ? val : key;
    }

    /** Возвращает привязку строки, автоматически обновляемую при смене языка. */
    public static StringBinding b(String key) {
        return Bindings.createStringBinding(() -> t(key), lang);
    }

    /** Форматирует число согласно локали текущего языка. */
    public static String formatNumber(Number n) {
        return NumberFormat.getInstance(lang.get().locale).format(n);
    }

    /** Форматирует дату и время согласно локали текущего языка. */
    public static String formatDate(LocalDateTime dt) {
        if (dt == null) return t("none");
        return DateTimeFormatter
                .ofLocalizedDateTime(FormatStyle.SHORT)
                .withLocale(lang.get().locale)
                .format(dt);
    }
